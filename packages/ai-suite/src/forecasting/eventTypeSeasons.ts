/**
 * מחזיר מקדם עונתי לסוג אירוע ותאריך.
 */
export function seasonalMultiplier(
  eventType: EventType,
  date: Date,
): number {
  const month = date.getMonth(); // 0-11
  return SEASONALITY[eventType][month];
}

/**
 * מקדם יום-בשבוע: רוב האירועים בישראל ביום ה'-ש' (חמישי-מוצ"ש).
 * 0=ראשון, 6=שבת. כאן: שבת=0 (אסור הלכתית לרוב), חמישי=שיא.
 */
export function dayOfWeekMultiplier(date: Date): number {
  const dow = date.getDay();
  const dowFactors = [0.7, 0.6, 0.65, 0.75, 1.6, 0.4, 0.2];
  return dowFactors[dow];
}
